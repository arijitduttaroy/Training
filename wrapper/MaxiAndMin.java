public class MaxiAndMin {

    public static void main(String[] args) {
        System.out.println("For Integer , the maximum and minimum value is \n");
        System.out.println("Max " + Integer.MAX_VALUE + "Min " + Integer.MIN_VALUE+"\n");

        System.out.println("For Double, the maximum and minimum value is \n");
        System.out.println("Max " + Double.MAX_VALUE +"Min" + Double.MIN_VALUE+"\n");

        System.out.println("For Float, the maximum and minimum value is \n");
        System.out.println("Max " + Float.MAX_VALUE + " Min" + Float.MIN_VALUE + "\n");

        System.out.println("For short , the maximum and minimum value is \n");
        System.out.println("Max "+ Short.MAX_VALUE+ "Min"+ Short.MIN_VALUE + "\n");

        System.out.println("For Long, the maximum and minimum value is \n");
        System.out.println("Max "+Long.MAX_VALUE+"Min" + Long.MIN_VALUE + "\n");

        System.out.println("For Byte, the maximum and minimum value is \n");
        System.out.println("Max " + Byte.MAX_VALUE +  "Min" + Byte.MIN_VALUE + "\n");
        
    }
}
